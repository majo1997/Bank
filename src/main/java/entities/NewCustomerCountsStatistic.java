package entities;

public class NewCustomerCountsStatistic {
    private Integer year;
    private Integer quadYear;
    private Integer count;

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Integer getQuadYear() {
        return quadYear;
    }

    public void setQuadYear(Integer quadYear) {
        this.quadYear = quadYear;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
